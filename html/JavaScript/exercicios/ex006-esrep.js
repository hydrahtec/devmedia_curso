var c = 1

console.log('inicio da estrutura de repetição while')
while (c<=5) {
    console.log(`Passo ${c}`)
    c++
}
console.log('fim da estrutura')

console.log('')

console.log('Inicio da estrutura de repetição do while')
do {
    console.log(`Passo ${c}`)
    c++
} while (c<=10)
console.log('Fim da estrutura')

console.log('')

console.log('Inicio da estrutura de repetição for')

for (c=1;c<=10;c++) {
    console.log(`Passo ${c}`)
}