// o tipo da variável é definido de acordo com o valor dela

let valor = 4;
console.log(typeof valor);
// number

let sobrenome = 'Silva';
console.log(typeof sobrenome);
// string

let ligado = true
console.log(typeof ligado);
// boolean

let obj = {nome: 'jose', peso: 130}
console.log(typeof obj);
// object

let funcao = () => {}
console.log(typeof funcao);
// function